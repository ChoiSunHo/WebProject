package Competition;

public class Competition {

	private int CompeNum;
	private String CompeTitle;
	private String CompeID;
	private String CompeDate;
	private String CompeContent;
	private int CompeAvailable;
	
	
	public int getCompeNum() {
		return CompeNum;
	}
	public void setCompeNum(int compeNum) {
		CompeNum = compeNum;
	}
	public String getCompeTitle() {
		return CompeTitle;
	}
	public void setCompeTitle(String compeTitle) {
		CompeTitle = compeTitle;
	}
	public String getCompeID() {
		return CompeID;
	}
	public void setCompeID(String compeID) {
		CompeID = compeID;
	}
	public String getCompeDate() {
		return CompeDate;
	}
	public void setCompeDate(String compeDate) {
		CompeDate = compeDate;
	}
	public String getCompeContent() {
		return CompeContent;
	}
	public void setCompeContent(String compeContent) {
		CompeContent = compeContent;
	}
	public int getCompeAvailable() {
		return CompeAvailable;
	}
	public void setCompeAvailable(int compeAvailable) {
		CompeAvailable = compeAvailable;
	}
}
